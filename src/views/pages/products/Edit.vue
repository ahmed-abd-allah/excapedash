<script>
import axiosApi from "../../../axios";
import { Validator } from "vee-validate";
const dict = {
  custom: {
    price: {
      required: "من فضلك حدد الثمن ",
      numeric: "السعر يحتوي علي أرقام فقط",
    },
    nameAr: {
      required: "  من فضلك حدد الإسم باللغة العربية",
    },
    nameEn: {
      required: "من فضلك حدد الإسم باللغة الإنجليزية",
    },
  },
};

// register custom messages
Validator.localize("ar", dict);
export default {
  data() {
    return {
      subCategory: null,
      category: null,
      product: null,
      photo: null,
      categories: [],
      subCategories: null,
      url: "",
    };
  },
  created() {
    console.log(this.$route.params.id);
    if (this.$route.params.id) {
      this.getProuct(this.$route.params.id);
    } else {
      this.$router.push({ path: "/products" });
    }
  },
  watch: {
    category(newC, oldC) {
      if (newC != oldC) {
        this.getSubcategories(this.category._id);
      }
    },
  },
  methods: {
    fileSelected(e) {
      this.product.image = "";
      this.photo = e.target.files[0];
      this.product.image = URL.createObjectURL(this.photo);
    },
    async uploadImage() {
      this.$vs.loading();
      if (this.photo != null&&!this.product.image.includes("res.cloudinary.com")) {
        let form_data = new FormData();
        form_data.append("image", this.photo);
        await axiosApi
          .post(`/resourses/image`, form_data, {
            headers: { "Content-Type": "multipart/form-data" },
          })
          .then((response) => {
            this.product.image = response.data.url;
            console.log(response);
            this.updateProduct();
          })
          .catch((e) => {
            console.log(e);
          });
      } 
      else if(this.product.image.includes("res.cloudinary.com")){
            this.updateProduct();

      }
      else {
              this.$vs.loading.close();

        this.$vs.notify({
          title: "ًعفوا",
          text: "من فضلك  ادخل صورة المنتج ",
          color: "warning",
          iconPack: "feather",
          position: "top-right",
          icon: "icon-alert-circle",
        });
      }
    },
    updateProduct() {
      this.$validator.validateAll().then((result) => {
        if (result ) {
          this.product.catId = this.category._id;
          this.product.subCatId = this.subCategory._id;
          axiosApi
            .patch(`/products/${this.product._id}`, this.product)
            .then((response) => {
              this.$router.push({ path: `/products` });

              this.$vs.loading.close();

              this.$vs.notify({
                title: "حسنا ",
                text: "تم تعديل المنتج   بنجاح",
                color: "success",
                iconPack: "feather",
                position: "top-right",
                icon: "icon-check",
              });
            })
            .catch((e) => {
              this.$vs.loading.close();
              //   console.log(e);
              this.$vs.notify({
                title: "ًعفوا",
                text: "حدث خطئ تاكد من اتصال بالانترنت ",
                color: "warning",
                iconPack: "feather",
                position: "top-right",
                icon: "icon-alert-circle",
              });
              console.log(e);
            });
        } else {
          this.$vs.notify({
            title: "ًعفوا",
            text: "من فضلك املاء كل البيانات  ",
            color: "warning",
            iconPack: "feather",
            position: "top-right",
            icon: "icon-alert-circle",
          });
        }
      });
    },
    deleteProduct() {
      this.$snotify.confirm(
        "في حالة مسح المنتج  لا يمكنك إسترجاعه مرة أخري",
        "هل أنت متأكد ؟",
        {
          showProgressBar: false,
          closeOnClick: false,
          pauseOnHover: true,
          buttons: [
            {
              text: "موافق",
              action: (toast) => {
                this.$snotify.remove(toast.id);
                this.$vs.loading();
                axiosApi
                  .delete(`/products/${this.$route.params.id}`)
                  .then((response) => {
                    this.$vs.loading.close();
                    this.$router.push({ path: "/products" });

                    this.$vs.notify({
                      title: "حسنا ",
                      text: "تم مسح المنتج  بنجاح",
                      color: "success",
                      iconPack: "feather",
                      position: "top-right",
                      icon: "icon-check",
                    });
                  })
                  .catch((e) => {
                    this.$vs.loading.close();
                  });
              },
              bold: true,
            },
            {
              text: "إلغاء",
              action: (toast) => {
                this.$snotify.remove(toast.id);
              },
              bold: true,
            },
          ],
        }
      );
    },
    async getProuct() {
      this.$vs.loading();
      await axiosApi
        .get(`/products/${this.$route.params.id}`)
        .then((res) => {
          this.$vs.loading.close();
          this.product = res.data.data;

          this.getcategories();
          this.getSubcategories(this.product.catId);
        })
        .catch((e) => {
          this.$vs.loading.close();
        });
    },
    getSubcategories(id) {
      this.$vs.loading();

      console.log(id);
      axiosApi.get(`subcategory/category/${id}`).then((response) => {
        console.log(response.data.data);
        this.subCategories = response.data.data;
        this.subCategory =
          this.subCategories.find((e) => e._id == this.product.subCatId) ||
          null;
        this.$vs.loading.close();
      });
    },

    getcategories() {
      this.$vs.loading();
      axiosApi
        .get(`category`)
        .then((response) => {
          this.categories = response.data.data;
          this.category = this.categories.find(
            (e) => e._id == this.product.catId
          );
          this.$vs.loading.close();
        })
        .catch((e) => {
          this.$vs.loading.close();

          console.log(e);
        });
    },
  },
};
</script>

